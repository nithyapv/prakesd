package aerd;


public class course {
	
	private int cid;
	
	private String cname;
	
	private double cfee;

	
	public void setCid(int cid) {
		this.cid = cid;
	}
	
	
	
	public int getCid() {
		return cid;
	}

	

	public void setCname(String cname) {
		this.cname = cname;
	}
	
	
	public String getCname() {
		return cname;
	}

	
	
	public void setCfee(double cfee) {
		this.cfee = cfee;
	}

	public double getCfee() {
		return cfee;
	}

	
	
	

}
